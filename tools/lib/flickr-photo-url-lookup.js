import Flickr from "flickrapi"
import Promise from "bluebird"
import fs from "fs"

const flickrOptions = {
  api_key: process.env.FLICKR_API_KEY,
  secret: process.env.FLICKR_SHARED_SECRET,
  user_id: process.env.FLICKR_USER_ID,
  access_token: process.env.FLICKR_ACCESS_TOKEN,
  access_token_secret: process.env.FLICKR_ACCESS_TOKEN_SECRET,
}

function lookupPhoto(id) {
  const filename = `flickr-data/${id}.json`
  if (fs.existsSync(filename)) { console.log("already fetched"); return }

  Flickr.tokenOnly(flickrOptions, (error, flickr) => (
    Promise.promisify(flickr.photos.getSizes)({ photo_id: id })
    .then(result => result.sizes.size)
    .then(sizeArray => sizeArray.reduce((memo, val) => { memo[val.label] = val; return memo}, {}))
    .then(sizes => {
      fs.writeFile(filename, JSON.stringify(sizes, null, 2), (err) => {
        if (err) throw err
      })
    })
    .catch(err => console.error(err))
  ))
}

